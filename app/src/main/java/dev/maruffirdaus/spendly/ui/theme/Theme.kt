    income = incomeLight,
    onIncome = onIncomeLight,
    incomeContainer = incomeContainerLight,
    onIncomeContainer = onIncomeContainerLight
)

val LocalExtendedColorScheme = compositionLocalOf { ExtendedLightColorScheme }

@Composable
fun SpendlyTheme(
    darkTheme: Boolean = isSystemInDarkTheme(),
    dynamicColor: Boolean = false,
    content: @Composable () -> Unit
) {
    val colorScheme = when {
        dynamicColor -> {
            val context = LocalContext.current
            if (darkTheme) dynamicDarkColorScheme(context) else dynamicLightColorScheme(context)
        }

        darkTheme -> DarkColorScheme
        else -> LightColorScheme
    }

    val extendedColorScheme = when {
        darkTheme -> ExtendedDarkColorScheme
        else -> ExtendedLightColorScheme
    }

    CompositionLocalProvider(LocalExtendedColorScheme provides extendedColorScheme) {
        MaterialTheme(
            colorScheme = colorScheme,
            typography = Typography,
            content = content
        )
    }
}